std::vector<double> getComparisonValues(eckit::PathName gribname, size_t expectedNumberOfValues) {
    std::vector<Range> ranges = {Range(0, expectedNumberOfValues)};
    std::vector<double> comparisonValues = eccodesExtract(gribname, {0}, ranges)[0][0];
    EXPECT(comparisonValues.size() == expectedNumberOfValues);
    return comparisonValues;
}

void test(eckit::PathName gribname){
    const std::vector<double> comparisonValues = getComparisonValues(gribname, expectedNumberOfValues);

    GribJump gj;

    {
        // Big ranges
        const std::vector<Range> ranges = {
            Range(0, 10000),  
            Range(100000, 110000), 
            Range(200000, 210000),
            Range(300000, 310000), Range(400000, 410000), Range(500000, 510000),
            // and read towards the end
            Range(6500000, 6510000),Range(6520000, 6530000),Range(6550000, 6560000),
            Range(6560000, expectedNumberOfValues),
        };

        const std::vector<eckit::Offset> offsets = {0};
        std::unique_ptr<ExtractionItem> output(std::move(gj.extract(gribname, offsets, {ranges})[0]));
        auto values = output->values();
        
        EXPECT(values.size() == ranges.size());

        // compare the values
        for (size_t i = 0; i < values.size(); i++) {
            size_t range0 = std::get<0>(ranges[i]);
            size_t range1 = std::get<1>(ranges[i]);
            EXPECT(values[i].size() == range1 - range0);
            for (size_t j = 0; j < values[i].size(); j++) {
                if (std::isnan(values[i][j])) {
                    EXPECT(comparisonValues[range0 + j] == 9999);
                    continue;
                }
                EXPECT(values[i][j] == comparisonValues[range0 + j]);
            }
        }
    }

    // Now, read 5000 single points
    std::vector<Range> singlePoints;
    for (size_t i = 0; i < 5000; i++) {
        size_t r0 = i*100;
        singlePoints.push_back(Range(r0, r0+1));
    }
    // Plus the last one
    singlePoints.push_back(Range(expectedNumberOfValues-1, expectedNumberOfValues));

    std::vector<eckit::Offset> offsets = {0};

    std::unique_ptr<ExtractionItem> output(std::move(gj.extract(gribname, offsets, {singlePoints})[0]));

    auto values = output->values();

    EXPECT(values.size() == singlePoints.size());

    // compare the values
    for (size_t i = 0; i < values.size(); i++) {
        size_t range0 = std::get<0>(singlePoints[i]);
        size_t range1 = std::get<1>(singlePoints[i]);
        EXPECT(values[i].size() == range1 - range0);
        for (size_t j = 0; j < values[i].size(); j++) {
            if (std::isnan(values[i][j])) {
                EXPECT(comparisonValues[range0 + j] == 9999);
                continue;
            }
            EXPECT(values[i][j] == comparisonValues[range0 + j]);
        }
    }

}

